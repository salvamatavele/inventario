function isEmpty(value) {
    if (value === "") 
    {
        return true;
    }else
    {
        return false;
    }
}

function mini(value, min) {
    if (value.lenght < min) {
        return true;
    } else {
        return false;
    }
}
function max(value, max) {
    if (value.lenght > max) {
        return true;
    } else {
        return false;
    }
}